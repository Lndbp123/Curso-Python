#tipo de dados boolean
#Ao questionar algo em um programa, a resposta pode ser apenas verdadeira ou falsa.
#Só existem duas respostas possiveis: verdadeiro ou falso.
#Existem varios operadoes para "questonar"
#Dentre eles o ==, que é operador logico que 
#questionar se um valor é igual a outro valor.


print(10 == 10)  # Retorna True, pois 10 é igual a 10
print(5 == 3)    # Retorna False, pois 5 não é igual a 3
#Outro operador logico é o !=, que questiona se um valor é diferente de outro valor.
print(10 != 5)  # Retorna True, pois 10 é diferente de 5
print(type(7 != 7))  # Retorna False, pois 7 não é diferente