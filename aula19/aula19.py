"""
Operadores de comparação (ou relacionais)
==  igual a
!=  diferente de    
>   maior que
>=  maior ou igual a    
<   menor que
<=  menor ou igual a
Esses operadores sempre retornam um valor booleano (True ou False)
Exemplos:
print(1 == 1)      # True
print(1 != 2)      # True       
print(3 > 2)       # True
print(3 >= 3)      # True
print(2 < 3)       # True
print(2 <= 2)      # True
print(1 == 2)      # False



"""
